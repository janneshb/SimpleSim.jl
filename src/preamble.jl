function print_preamble()
    println(OUT_STREAM, "   ___ _            _     ___ _          _ _ ")
    println(OUT_STREAM, "  / __(_)_ __  _ __| |___/ __(_)_ __    (_) |")
    println(OUT_STREAM, "  \\__ \\ | '  \\| '_ \\ / -_)__ \\ | '  \\ _ | | |")
    println(OUT_STREAM, "  |___/_|_|_|_| .__/_\\___|___/_|_|_|_(_)/ |_|")
    println(OUT_STREAM, "              |_|                     |__/   ")
    println(OUT_STREAM, "  ------------------------------------------- \n")
    println(OUT_STREAM, "  (c) $YEAR $AUTHORS          v$VERSION\n")
end
